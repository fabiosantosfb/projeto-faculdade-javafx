package Model;

public class Telefone {
    private String telefone;
    private String ddd;

    public Telefone() {
    }

    /**
     * @return
     */
    public String getTelefone() {
        return telefone;
    }

    /**
     * @param {telefone}
     */
    public void setTelefone(String telefone) {
        this.telefone = telefone;
    }

    /**
     * @return
     */
    public String getDdd() {
        return ddd;
    }

    /**
     * @param {ddd}
     */
    public void setDdd(String ddd) {
        this.ddd = ddd;
    }
}
